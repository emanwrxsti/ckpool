#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "pow.h"
#include "sha512_256.h"

static void require_equal(const char *name, const unsigned char *actual,
			  const unsigned char *expected, size_t len)
{
	size_t i;

	if (!memcmp(actual, expected, len))
		return;

	fprintf(stderr, "%s failed\nactual:   ", name);
	for (i = 0; i < len; i++)
		fprintf(stderr, "%02x", actual[i]);
	fprintf(stderr, "\nexpected: ");
	for (i = 0; i < len; i++)
		fprintf(stderr, "%02x", expected[i]);
	fprintf(stderr, "\n");
	exit(EXIT_FAILURE);
}

int main(void)
{
	unsigned char digest[32];
	pow_algo_t algo;
	static const unsigned char abc_expected[32] = {
		0x53, 0x04, 0x8e, 0x26, 0x81, 0x94, 0x1e, 0xf9,
		0x9b, 0x2e, 0x29, 0xb7, 0x6b, 0x4c, 0x7d, 0xab,
		0xe4, 0xc2, 0xd0, 0xc6, 0x34, 0xfc, 0x6d, 0x46,
		0xe0, 0xe2, 0xf1, 0x31, 0x07, 0xe7, 0xaf, 0x23
	};
	static const unsigned char abc_double_expected[32] = {
		0x43, 0xd4, 0x1a, 0x15, 0x66, 0x87, 0x5f, 0xf3,
		0xdb, 0x7d, 0xae, 0x19, 0xaa, 0x7f, 0x3f, 0x68,
		0x96, 0x6a, 0xbf, 0xa6, 0x98, 0x41, 0x41, 0x82,
		0x96, 0xa6, 0xfd, 0xc9, 0xb2, 0xba, 0x18, 0xee
	};
	/* Radiant genesis header in wire serialization order. */
	static const unsigned char radiant_genesis_header[80] = {
		0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
		0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
		0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
		0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
		0x00,0x00,0x00,0x00,0x37,0x2c,0xba,0xf8,
		0x97,0x94,0xae,0xed,0x5e,0x71,0x1b,0x02,
		0xe7,0x8e,0xc4,0x50,0x2a,0xd8,0xb3,0x15,
		0xa9,0x87,0xc2,0xe2,0x75,0x8a,0x85,0xe3,
		0x6a,0x3f,0x7c,0x02,0xaa,0xde,0xaf,0x62,
		0xff,0xff,0x00,0x1d,0x79,0x80,0xb7,0x2a
	};
	static const unsigned char radiant_genesis_digest[32] = {
		0xb4,0x3f,0x00,0x4e,0x6f,0x7a,0x1f,0x7e,
		0x43,0x9e,0xa5,0x0c,0x6c,0x2a,0xac,0x60,
		0xb6,0xff,0xb3,0x76,0x68,0x8d,0xe2,0x8b,
		0x5d,0xed,0xd8,0x65,0x00,0x00,0x00,0x00
	};

	sha512_256((const unsigned char *)"abc", 3, digest);
	require_equal("SHA-512/256 abc", digest, abc_expected, sizeof(digest));

	sha512_256d((const unsigned char *)"abc", 3, digest);
	require_equal("SHA-512/256d abc", digest, abc_double_expected, sizeof(digest));

	pow_hash(POW_ALGO_SHA512_256D, radiant_genesis_header,
		 sizeof(radiant_genesis_header), digest);
	require_equal("Radiant genesis PoW", digest, radiant_genesis_digest,
		      sizeof(digest));

	if (!pow_algo_parse("radiant", &algo) || algo != POW_ALGO_SHA512_256D) {
		fprintf(stderr, "Radiant algorithm alias failed\n");
		return EXIT_FAILURE;
	}
	if (!pow_algo_parse("sha256d", &algo) || algo != POW_ALGO_SHA256D) {
		fprintf(stderr, "SHA256d algorithm parsing failed\n");
		return EXIT_FAILURE;
	}
	if (pow_algo_parse("not-a-real-algo", &algo)) {
		fprintf(stderr, "Invalid algorithm was accepted\n");
		return EXIT_FAILURE;
	}

	printf("All PoW tests passed, including the Radiant genesis header.\n");
	return EXIT_SUCCESS;
}
